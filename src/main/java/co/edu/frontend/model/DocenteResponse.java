package co.edu.frontend.model;

public class DocenteResponse {
    private Long codigoDocente;
    private String experiencia;
    private String nombrePersona;
    private String nombrePrograma;

    public Long getCodigoDocente() { return codigoDocente; }
    public void setCodigoDocente(Long codigoDocente) { this.codigoDocente = codigoDocente; }
    public String getExperiencia() { return experiencia; }
    public void setExperiencia(String experiencia) { this.experiencia = experiencia; }
    public String getNombrePersona() { return nombrePersona; }
    public void setNombrePersona(String nombrePersona) { this.nombrePersona = nombrePersona; }
    public String getNombrePrograma() { return nombrePrograma; }
    public void setNombrePrograma(String nombrePrograma) { this.nombrePrograma = nombrePrograma; }
}
